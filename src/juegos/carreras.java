    public synchronized void actualizarTablaPosiciones() {
        class PosCarro {

            String nombre;
            int x;

            PosCarro(String n, int x) {
                this.nombre = n;
                this.x = x;
            }
        }

        List<PosCarro> lista = new ArrayList<>();
        lista.add(new PosCarro(nombre1, Carro1.getX()));
        lista.add(new PosCarro(nombre2, Carro2.getX()));
        lista.add(new PosCarro(nombre3, Carro3.getX()));

        lista.sort((a, b) -> Integer.compare(b.x, a.x));

        StringBuilder sb = new StringBuilder();
        for (int i = 0; i < lista.size(); i++) {
            PosCarro p = lista.get(i);
            sb.append("#").append(i + 1).append(". ")
                    .append(p.nombre)
                    .append(" (").append(p.x).append(")\n");
        }

        tabla.setText(sb.toString());
    }

    public synchronized void notificarGanador(JLabel carro) {

        if (!carreraActiva) {
            return;
        }

        String nombre = "";

        if (carro == Carro1) {
            nombre = nombre1;
        } else if (carro == Carro2) {
            nombre = nombre2;
        } else if (carro == Carro3) {
            nombre = nombre3;
        }

        if (!ordenLlegada.contains(nombre)) {
            ordenLlegada.add(nombre);
        }

        // Cuando llegan los 3 carros termina la carrera
        if (ordenLlegada.size() == 3) {
            carreraActiva = false;

            // Actualizar tabla final con ranking
            StringBuilder sbTabla = new StringBuilder();
            for (int i = 0; i < ordenLlegada.size(); i++) {
                sbTabla.append("#").append(i + 1).append(". ")
                        .append(ordenLlegada.get(i)).append("\n");
            }
            tabla.setText(sbTabla.toString());

            //Mostrar los resultados
            StringBuilder sb = new StringBuilder("Resultados finales:\n\n");
            for (int i = 0; i < ordenLlegada.size(); i++) {
                sb.append(i + 1).append(". ")
                        .append(ordenLlegada.get(i)).append("\n");
            }

            mostrarDialogoBonito(sb.toString(), "", carrito);

            // Resetea las posiciones
            Carro1.setLocation(30, 20);
            Carro2.setLocation(30, 100);
            Carro3.setLocation(30, 170);

            Reiniciar.setEnabled(true);
        }
    }

    // Inicia la carrera: resetea todo y arranca los hilos
    private void iniciarCarrera() {

        if (carreraActiva) {
            return;
        }

        Reiniciar.setEnabled(false);
        tabla.setText("");

        ordenLlegada.clear();
        carreraActiva = true;

        Carro1.setLocation(30, 20);
        Carro2.setLocation(30, 100);
        Carro3.setLocation(30, 170);

        t1 = new CarroThread(Carro1, MAX_POSICION, this);
        t2 = new CarroThread(Carro2, MAX_POSICION, this);
        t3 = new CarroThread(Carro3, MAX_POSICION, this);

        t1.start();
        t2.start();
        t3.start();
    }

    public void mostrarDialogoBonito(String mensaje, String titulo, Icon icono) {

        JPanel panel = new JPanel();
        panel.setLayout(new BoxLayout(panel, BoxLayout.Y_AXIS));
        panel.setBackground(new Color(245, 245, 255));
        panel.setBorder(BorderFactory.createEmptyBorder(20, 30, 20, 30));

        final float[] hue = {0f};
        Timer bordeTimer = new Timer(40, e -> {
            hue[0] += 0.01f;
            if (hue[0] > 1) {
                hue[0] = 0;
            }
            Color rainbow = Color.getHSBColor(hue[0], 1f, 1f);
            panel.setBorder(BorderFactory.createCompoundBorder(
                    BorderFactory.createLineBorder(rainbow, 4, true),
                    BorderFactory.createEmptyBorder(20, 30, 20, 30)
            ));
            panel.repaint();
        });
        bordeTimer.start();

        JLabel texto = new JLabel(
                "<html><div style='width:260px; text-align:center;'>"
                + mensaje.replace("\n", "<br>")
                + "</div></html>",
                icono,
                JLabel.CENTER
        );
        texto.setFont(new Font("Segoe UI", Font.PLAIN, 15));
        texto.setForeground(new Color(60, 60, 60));
        texto.setAlignmentX(JLabel.CENTER_ALIGNMENT);

        panel.add(texto);
        panel.add(Box.createVerticalStrut(15));

        JButton ok = new JButton("OK");
        ok.setFont(new Font("Segoe UI", Font.BOLD, 13));
        ok.setPreferredSize(new Dimension(50, 36));
        ok.setMaximumSize(new Dimension(50, 36));
        ok.setFocusPainted(false);
        ok.setBackground(new Color(210, 200, 255));
        ok.setAlignmentX(JButton.CENTER_ALIGNMENT);

        ok.addMouseListener(new java.awt.event.MouseAdapter() {
            public void mouseEntered(java.awt.event.MouseEvent e) {
                ok.setBackground(new Color(225, 215, 255));
            }

            public void mouseExited(java.awt.event.MouseEvent e) {
                ok.setBackground(new Color(210, 200, 255));
            }
        });

        panel.add(ok);

        JDialog dialog = new JDialog(this, titulo, true);
        dialog.setUndecorated(true);
        dialog.setContentPane(panel);
        dialog.pack();
        dialog.setLocationRelativeTo(this);

        dialog.getRootPane()
                .getInputMap(JComponent.WHEN_IN_FOCUSED_WINDOW)
                .put(KeyStroke.getKeyStroke("ESCAPE"), "none");

        ok.addActionListener(e -> {
            bordeTimer.stop();
            dialog.dispose();
        });

        dialog.setVisible(true);
    }

